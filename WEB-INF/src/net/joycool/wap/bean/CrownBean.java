package net.joycool.wap.bean;

public class CrownBean {
	private int id;

	private int imageId;

	private String image;

	private int imagePathId;

	/**
	 * @return Returns the id.
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 *            The id to set.
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return Returns the image.
	 */
	public String getImage() {
		return image;
	}

	/**
	 * @param image
	 *            The image to set.
	 */
	public void setImage(String image) {
		this.image = image;
	}

	/**
	 * @return Returns the imageId.
	 */
	public int getImageId() {
		return imageId;
	}

	/**
	 * @param imageId
	 *            The imageId to set.
	 */
	public void setImageId(int imageId) {
		this.imageId = imageId;
	}

	/**
	 * @return Returns the imagePathId.
	 */
	public int getImagePathId() {
		return imagePathId;
	}

	/**
	 * @param imagePathId
	 *            The imagePathId to set.
	 */
	public void setImagePathId(int imagePathId) {
		this.imagePathId = imagePathId;
	}

}
