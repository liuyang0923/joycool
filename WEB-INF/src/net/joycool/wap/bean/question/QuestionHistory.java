package net.joycool.wap.bean.question;

public class QuestionHistory {
	int id;
	int today2;
	int today;
	int total;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getToday() {
		return today;
	}
	public void setToday(int today) {
		this.today = today;
	}
	public int getToday2() {
		return today2;
	}
	public void setToday2(int today2) {
		this.today2 = today2;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
}
