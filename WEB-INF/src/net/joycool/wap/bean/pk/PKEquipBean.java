	public String toDetail() {
		StringBuffer sb = new StringBuffer();
		sb.append("<img src=\"" + PKAction.PK_IMAGE_PATH + "/equip/"
				+ id + ".gif\" alt=\"\"/>");
		sb.append("<br/>");
		sb.append(name);
		sb.append("<br/>");
		sb.append("价格:");
		sb.append(price);
		sb.append("<br/>");
		if(physicalGrowthRadix > 0) {
			sb.append("体力+");
			sb.append(physicalGrowthRadix);
			sb.append("<br/>");
		}
		if(energyGrowthRadix > 0) {
			sb.append("气力+");
			sb.append(energyGrowthRadix);
			sb.append("<br/>");
		}
		int max = aggressGrowthRadix + aggressMaxRadix;
		if(max > 0) {
			sb.append("攻击力+");
			sb.append(aggressGrowthRadix);
			sb.append("&nbsp;-&nbsp;");
			sb.append(max);
			sb.append("<br/>");
		}
		max = recoveryGrowthRadix+recoveryMaxRadix ;
		if(max > 0) {
			sb.append("防御力+");
			sb.append(recoveryGrowthRadix);
			sb.append("&nbsp;-&nbsp;");
			sb.append(max);
			sb.append("<br/>");
		}
		if(flyingGrowthRadix > 0) {
			sb.append("轻功+");
			sb.append(flyingGrowthRadix);
			sb.append("<br/>");
		}
		if(luckGrowthRadix > 0) {
			sb.append("吉运+");
			sb.append(luckGrowthRadix);
			sb.append("<br/>");
		}
		return sb.toString();
	}
