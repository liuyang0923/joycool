package net.joycool.wap.bean.job;

public class SpiritBean {
	int id;

	String title;// 按键名称

	String description;// 描述

	String effectdes;// 效果说明

	int price;// 花费

	int effect;// 增加反倭值或者清零

	/**
	 * @return Returns the description.
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * @param description The description to set.
	 */
	public void setDescription(String description) {
		this.description = description;
	}

	/**
	 * @return Returns the effect.
	 */
	public int getEffect() {
		return effect;
	}

	/**
	 * @param effect The effect to set.
	 */
	public void setEffect(int effect) {
		this.effect = effect;
	}

	/**
	 * @return Returns the effectdes.
	 */
	public String getEffectdes() {
		return effectdes;
	}

	/**
	 * @param effectdes The effectdes to set.
	 */
	public void setEffectdes(String effectdes) {
		this.effectdes = effectdes;
	}

	/**
	 * @return Returns the id.
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id The id to set.
	 */
	public void setId(int id) {
		this.id = id;
	}


	/**
	 * @return Returns the title.
	 */
	public String getTitle() {
		return title;
	}

	/**
	 * @param title The title to set.
	 */
	public void setTitle(String title) {
		this.title = title;
	}

	/**
	 * @return Returns the price.
	 */
	public int getPrice() {
		return price;
	}

	/**
	 * @param price The price to set.
	 */
	public void setPrice(int price) {
		this.price = price;
	}

	

}
