package net.joycool.wap.bean.job;

public class HuntWeaponBean {
	
	int id;

	String name;

	int price;

	int shotPrice;

	int hitRate;

	int notHitRate;

	int harmRate;

	/**
	 * @return Returns the harmRate.
	 */
	public int getHarmRate() {
		return harmRate;
	}

	/**
	 * @param harmRate
	 *            The harmRate to set.
	 */
	public void setHarmRate(int harmRate) {
		this.harmRate = harmRate;
	}



	/**
	 * @return Returns the hitRate.
	 */
	public int getHitRate() {
		return hitRate;
	}

	/**
	 * @param hitRate The hitRate to set.
	 */
	public void setHitRate(int hitRate) {
		this.hitRate = hitRate;
	}

	/**
	 * @return Returns the id.
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 *            The id to set.
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return Returns the name.
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name The name to set.
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return Returns the notHitRate.
	 */
	public int getNotHitRate() {
		return notHitRate;
	}

	/**
	 * @param notHitRate
	 *            The notHitRate to set.
	 */
	public void setNotHitRate(int notHitRate) {
		this.notHitRate = notHitRate;
	}

	/**
	 * @return Returns the price.
	 */
	public int getPrice() {
		return price;
	}

	/**
	 * @param price
	 *            The price to set.
	 */
	public void setPrice(int price) {
		this.price = price;
	}

	/**
	 * @return Returns the shotPrice.
	 */
	public int getShotPrice() {
		return shotPrice;
	}

	/**
	 * @param shotPrice
	 *            The shotPrice to set.
	 */
	public void setShotPrice(int shotPrice) {
		this.shotPrice = shotPrice;
	}

}
