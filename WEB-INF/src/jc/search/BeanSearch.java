package jc.search;

public class BeanSearch {
	private int city = 0;
	private int astro = 0;
	private int gender = 0;
	private int aim = 0;

	public int getCity() {
		return city;
	}

	public void setCity(int city) {
		this.city = city;
	}

	public int getAstro() {
		return astro;
	}

	public void setAstro(int astro) {
		this.astro = astro;
	}

	public int getGender() {
		return gender;
	}

	public void setGender(int gender) {
		this.gender = gender;
	}

	public int getAim() {
		return aim;
	}

	public void setAim(int aim) {
		this.aim = aim;
	}

}
