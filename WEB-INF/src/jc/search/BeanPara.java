package jc.search;

public class BeanPara {
	int birYear;// 出生年
	int birMonth;// 出生月
	int birDay;// 出生日
	int city;// 同城
	int gender;// 异性
	int age;// 年龄
	int astro;// 星座
	int aim;// 情感,目的
	int type;// 记录用户所在搜索页面1.首页(综合搜索)、2.交友、3.老乡、4.星座、5.高级搜索

	public int getBirYear() {
		return birYear;
	}

	public void setBirYear(int birYear) {
		this.birYear = birYear;
	}

	public int getBirMonth() {
		return birMonth;
	}

	public void setBirMonth(int birMonth) {
		this.birMonth = birMonth;
	}

	public int getBirDay() {
		return birDay;
	}

	public void setBirDay(int birDay) {
		this.birDay = birDay;
	}

	public int getCity() {
		return city;
	}

	public void setCity(int city) {
		this.city = city;
	}

	public int getGender() {
		return gender;
	}

	public void setGender(int gender) {
		this.gender = gender;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getAstro() {
		return astro;
	}

	public void setAstro(int astro) {
		this.astro = astro;
	}

	public int getAim() {
		return aim;
	}

	public void setAim(int aim) {
		this.aim = aim;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}
}
