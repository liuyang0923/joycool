package jc.credit;

public class UserLooks {
	int userId;
	int bodyType;
	int looks;
	int charm;
	int point;
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public int getBodyType() {
		return bodyType;
	}
	public void setBodyType(int bodyType) {
		this.bodyType = bodyType;
	}
	public int getLooks() {
		return looks;
	}
	public void setLooks(int looks) {
		this.looks = looks;
	}
	public int getCharm() {
		return charm;
	}
	public void setCharm(int charm) {
		this.charm = charm;
	}
}
