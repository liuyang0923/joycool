package jc.credit;

public class CreditCity {
	int id;
	String city;
	int hypo;
	int flag;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public int getHypo() {
		return hypo;
	}
	public void setHypo(int hypo) {
		this.hypo = hypo;
	}
	public int getFlag() {
		return flag;
	}
	public void setFlag(int flag) {
		this.flag = flag;
	}
}
